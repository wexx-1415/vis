const getDatas = (
	data: {
		province: string;
		'PM2.5': number;
		PM10: number;
		SO2: number;
		NO2: number;
		CO: number;
		O3: number;
		month: number;
		area: string;
	}[],
	area?: string
) => {
	const pollutions = ['PM2.5', 'PM10', 'SO2', 'NO2', 'CO', 'O3'];
	const datas = {};
	pollutions.forEach((pollution) => {
		datas[pollution] = new Array(12).fill(0);
	});
	if (area) data = data.filter((d) => d.area === area || d.province === area);
	data.forEach((d) => {
		pollutions.forEach((pollution) => {
			datas[pollution][d.month - 1] += d[pollution];
		});
	});
	for (const [key, value] of Object.entries(datas)) {
		let v = [];
		let max = Math.max(...value);
		let min = Math.min(...value);
		value.forEach((e) => {
			v.push(((e - min) / (max - min)) * 100);
		});
		datas[key] = v;
	}
	return datas;
};
